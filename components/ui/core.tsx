// --- Badge ---
export interface BadgeProps extends React.HTMLAttributes<HTMLDivElement> {
  variant?: 'default' | 'secondary' | 'destructive' | 'outline' | 'success' | 'warning';
  className?: string;
}

export const Badge = ({ className, variant = "default", ...props }: BadgeProps) => {
  const variants = {
    default: "border-transparent bg-indigo-500/10 text-indigo-300 hover:bg-indigo-500/20 border-indigo-500/20",
    secondary: "border-transparent bg-secondary text-secondary-foreground hover:bg-secondary/80",
    destructive: "border-transparent bg-red-500/10 text-red-400 border-red-500/20",
    success: "border-transparent bg-green-500/10 text-green-400 border-green-500/20",
    warning: "border-transparent bg-amber-500/10 text-amber-400 border-amber-500/20",
    outline: "text-foreground",
  };

  return (
    <div className={cn("inline-flex items-center rounded-full border px-2.5 py-0.5 text-[10px] font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2", variants[variant], className)} {...props} />
  );
};